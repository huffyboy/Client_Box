package com.example.huff6.clientbox;

/**
 *
 */
public class Client {

    private String name;
    private String number;
    private int id;

    Client () {
        name = "";
        number = "";
        id = 0;
    }

    public String getName() {
        return name;
    }

    public String getNum() {
        return number;
    }

    public int getId() {
        return id;
    }
}
